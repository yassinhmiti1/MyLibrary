<script>
  document.addEventListener("DOMContentLoaded", function () {
    const navItems = document.querySelectorAll(".nav_container ul li.nav-item");
    const closeBtn = document.getElementById("close_nav");
    const sidebar = document.getElementById("sidebarMenu");
    const closeIcon = document.getElementById("close_icon");

    if (closeBtn && sidebar) {
      closeBtn.addEventListener("click", function () {
        sidebar.classList.toggle("collapsed");

        const mainContainer = document.querySelector(".maincontainer");

        if (sidebar.classList.contains("collapsed")) {
          if (mainContainer) mainContainer.style.gridTemplateColumns = "70px 1fr";
          closeIcon.innerHTML = '<path d="M120-240v-80h720v80H120Zm0-200v-80h720v80H120Zm0-200v-80h720v80H120Z"/>';
        } else {

          if (mainContainer) mainContainer.style.gridTemplateColumns = "260px 1fr";
          closeIcon.innerHTML = '<path d="m256-200-56-56 224-224-224-224 56-56 224 224 224-224 56 56-224 224 224 224-56 56-224-224-224 224Z" />';
        }
      });
    }

    function updateActiveMenu() {
      const currentHash = window.location.hash;

      navItems.forEach(item => {
        const link = item.querySelector("a");
        if (link) {
          const hrefAttribute = link.getAttribute("href");
          const linkHash = hrefAttribute.substring(hrefAttribute.indexOf("#"));

          if (currentHash === linkHash) {
            item.classList.add("active");
          } else if (!currentHash && linkHash === "#Livres") {
            item.classList.add("active");
          } else {
            item.classList.remove("active");
          }
        }
      });
    }

    updateActiveMenu();
    window.addEventListener("hashchange",
      updateActiveMenu);
  });


</script>